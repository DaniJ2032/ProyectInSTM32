int main(void) {

  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_USART3_UART_Init();

  char hello[] = "Hola Mundo\r\n";

  while (1){
    HAL_UART_Transmit(&huart3, hello, 13, 1);
    HAL_Delay(500); // Espera 1 segundo antes de enviar el siguiente mensaje    
  }

}

